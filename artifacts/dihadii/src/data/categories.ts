export interface ServiceCategory {
  slug: string;
  name: string;
  hindi: string;
  img: string;
  /**
   * object-position for `img`. Source photos are portrait (1122x1402) with the
   * subject's head near the top of the frame. Every place this image is
   * rendered (nav dropdown, service cards, category hero) crops it into a
   * shorter box, so this value is tuned per photo to keep the face/head fully
   * visible instead of relying on a single shared "center" crop.
   */
  imagePosition: string;
  description: string;
  /** What this worker's daily-wage/task-based scope typically covers. */
  covers: string[];
  /** Work that is clearly out of scope, or only included if separately agreed. */
  excludes?: string[];
  /** Things to clarify/agree with the worker upfront (timing, materials, extra duties, etc.). */
  notes?: string[];
  /** Safety guidance specific to this line of work. */
  safetyNotes?: string[];
}

